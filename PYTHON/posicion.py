# Programa para mostrar la entrada seleccionada según la posición del selector

selector = int(input("Ingrese la posición del selector (1, 2, 3 o 4): "))

if selector == 1:
    print("Entrada 1 seleccionada.")
elif selector == 2:
    print("Entrada 2 seleccionada.")
elif selector == 3:
    print("Entrada 3 seleccionada.")
elif selector == 4:
    print("Entrada 4 seleccionada.")
else:
    print("Opción inválida.")
